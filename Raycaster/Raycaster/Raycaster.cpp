void drawMap()
{
	int x, y, xo, yo;
	for (int y = 0; y < mapY; y++)
	{
		for (int x = 0; x < mapX; x++)
		{
			if (mapW[y * mapX + x] > 0)
			{
				glColor3f(1, 1, 1);
			}
			else
			{
				glColor3f(0.2, 0.2, 0.2);
			}
			glBegin(GL_QUADS);
			xo = x * mapS; yo = y * mapS;
			glVertex2f(xo+1, yo+1);
			glVertex2f(xo+1, yo+mapS-1);
			glVertex2f(xo+mapS-1, yo+mapS-1);
			glVertex2f(xo+mapS-1, yo+1);
			glEnd();
		}
	}
}

